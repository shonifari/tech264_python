# Task: Find out what happens when you convert a string to a boolean

# When does a string convert to False?
# - When it is an empty string
empty_string = ""
if not empty_string:
    print("String is converted to False")


# When does a string convert to True?
# - When it contains at least 1 character.
string = "Something"
if not string:
    print("String is converted to True")

